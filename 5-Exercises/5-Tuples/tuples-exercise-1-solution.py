## Tuples. Exercise #1 solution:

# Create a variable with name 'numbers' which is a tuple
# with the values 1, 2, 3 and 4 inside.
numbers=(1,2,3,4)
print(numbers)

# Create a variable with name 'number' which is a tuple
# with the value 1 inside.
number=(1,)
print(number)

# Create a variable with name 'constants' which is
# the result of the 'numbers' variable converted to a tuple
numbers = [10,20,30]
constants=tuple(numbers)
print(constants)